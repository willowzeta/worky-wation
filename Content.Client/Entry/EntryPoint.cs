        public override void PreInit()
        {
            ClientContentIoC.Register(Dependencies);

            foreach (var callback in TestingCallbacks)
            {
                var cast = (ClientModuleTestingCallbacks) callback;
                cast.ClientBeforeIoC?.Invoke();
            }
        }

